package entity;

public class Users {
	private String users_id;
	private String users_name;
	private String users_tel;
	private String users_sex;
	
	public String getUsers_id() {
		return users_id;
	}
	public void setUsers_id(String users_id) {
		this.users_id = users_id;
	}
	public String getUsers_name() {
		return users_name;
	}
	public void setUsers_name(String users_name) {
		this.users_name = users_name;
	}
	public String getUsers_tel() {
		return users_tel;
	}
	public void setUsers_tel(String users_tel) {
		this.users_tel = users_tel;
	}
	public String getUsers_sex() {
		return users_sex;
	}
	public void setUsers_sex(String users_sex) {
		this.users_sex = users_sex;
	}
	
	

}
